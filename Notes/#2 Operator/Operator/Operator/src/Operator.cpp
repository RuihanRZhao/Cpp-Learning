#include<iostream>
int main() {
	int a = 0;
	a = 3 + 2;//summation 
	a = 3 - 2;//subtraction
	a = 3 * 2;//multiplication
	a = 3 / 2;//division
	a = 3 % 2;//modulus
	a += 5;//a = a + 5
	a -= 5;//a = a - 5
	a *= 5;//a = a *5
	a /= 5;//a = a / 5
	a %= 5;//a = a % 5
	a++;//While this line, a remains. When this line end, a = a + 1
	++a;//While this line, a = a + 1. After this line, a remains.
	a--;//While this line, a remains. When this line end, a = a - 1
	--a;//While this line, a = a - 1. After this line, a remains.
	return 0;
}